#include "libft_type.h"

int		ft_isint(char *s)
{
	int		i;

	if (s)
	{
		i = 0;
		while (*(s + i) && ft_isspace(*(s + i)) &&
				(*(s + i) == '+' || *(s + i) == '-'))
			++i;
		if (*(s + i) && ft_isdigit(*(s + i)))
			return (1);
		return (0);
	}
	return (0);
}
