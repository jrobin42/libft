#include "ft_printf.h"

int		check_percent(char *s)
{
	int		i;

	i = 0;
	if (s)
		while (*s)
		{
			if (*s == '%')
				return (i);
			++s;
			++i;
		}
	return (-1);
}

int		enough_space(char *s)
{
	return (s && *s && *(s + 1) && ((*s == '0' && *(s + 1) == '0')
			|| ((*s == ' ') && *(s + 1) == ' ')));
}

int		enough_space2(char *s)
{
	return (s && *s && *(s + 1) && ((*s == ' ') && *(s + 1) == ' '));
}
